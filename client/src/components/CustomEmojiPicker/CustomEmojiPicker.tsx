export function CustomEmojiPicker({ onEmojiClick }: CustomEmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState(1);
  const [searchQuery, setSearchQuery] = useState('');
  const [recentEmojis, setRecentEmojis] = useState<string[]>(() => {
    const saved = localStorage.getItem('recentEmojis');
    return saved ? JSON.parse(saved) : [];
  });

  const handleEmojiSelect = (emoji: string) => {
    onEmojiClick(emoji);
    
    // Update recent emojis
    const updated = [emoji, ...recentEmojis.filter(e => e !== emoji)].slice(0, 24);
    setRecentEmojis(updated);
    localStorage.setItem('recentEmojis', JSON.stringify(updated));
  };

  // Update recent emojis category
  const categories = [...EMOJI_CATEGORIES];
  categories[0].emojis = recentEmojis;

  // Filter emojis by search
  const filteredEmojis = searchQuery 
    ? categories.flatMap(cat => cat.emojis).filter(emoji => emoji.includes(searchQuery))
    : categories[activeCategory]?.emojis || [];

  return (
    <div className="custom-emoji-picker">
      <div className="emoji-search">
        <Search size={16} />
        <input
          type="text"
          placeholder="Szukaj emoji..."
          value={searchQuery}
          onChange={(e) => setSearchQuery(e.target.value)}
        />
      </div>

      <div className="emoji-categories">
        {categories.map((category, index) => (
          <button
            key={category.name}
            className={`category-btn ${activeCategory === index ? 'active' : ''} ${index === 0 && recentEmojis.length === 0 ? 'disabled' : ''}`}
            onClick={() => setActiveCategory(index)}
            disabled={index === 0 && recentEmojis.length === 0}
            title={category.name}
          >
            {category.icon}
          </button>
        ))}
      </div>

      <div className="emoji-grid">
        {filteredEmojis.length > 0 ? (
          filteredEmojis.map((emoji, index) => (
            <button
              key={`${emoji}-${index}`}
              className="emoji-btn"
              onClick={() => handleEmojiSelect(emoji)}
              title={emoji}
            >
              {emoji}
            </button>
          ))
        ) : (
          <div className="emoji-empty">
            {searchQuery ? 'Nie znaleziono emoji' : activeCategory === 0 ? 'Brak ostatnio używanych' : 'Brak emoji w tej kategorii'}
          </div>
        )}
      </div>
    </div>
  );
}
